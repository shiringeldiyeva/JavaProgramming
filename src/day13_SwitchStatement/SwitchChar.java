package day13_SwitchStatement;

public class SwitchChar {
    public static void main(String[] args) {
        char women = 'c';
        switch (women){
            case 'a':
                System.out.println("the best");
                break;
            case 'b':
                System.out.println("modern");
                break;
            case 'c':
            System.out.println("hungry");
            break;
            default:
                System.out.println("nor a women");
                break;

        }

    }
}
