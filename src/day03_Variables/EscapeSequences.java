package day03_Variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Shirin, I am from Turkmenistan");

        System.out.println("==================");
      // \t :
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

      // || : \
        System.out.println("/");  //
        System.out.println("\\"); // prints single backslash

        System.out.println("hello Everyone/n My name is Shirin, I am from Turkmenistan");

    }
}






