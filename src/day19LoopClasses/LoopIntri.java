package day19LoopClasses;

public class LoopIntri {
    public static void main(String[] args) {
        for(int i=1; i<=10;i++){
            System.out.println("Hello");
        }
        System.out.println("==============================");
        for (int i=1; i<=10; i++ ){
            System.out.println("Shirin");
        }
        System.out.println("+++++++++++++++++++++++");
        for (int i=0; i<=1000;i++){
            System.out.println(i);
        }
        System.out.println("===========================");


    }

}
